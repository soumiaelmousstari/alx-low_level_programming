#include <stdio.h>
/**
 * main -Write a program that prints all the numbers of base 16
 * in lowercase, followed by a new line.
 * Return: 0 saccucc instructions
 */
int main(void)
{
	int n;
	int m;

	for (n = 48; n < 58; n++)
		putchar(n);
	for (m = 97; m < 103; m++)
		putchar(m);
	putchar('\n');
return (0);
}
